
package LearnerPylonDemoV2;


public class Stu_acco {
    
    private double s_fees;
    private double cd_fees;
    private double r_fees;
    private double ex_fees;
    private double g_fees;

    public Stu_acco(double s_fees, double cd_fees, double r_fees, double ex_fees, double g_fees) {
        this.s_fees = s_fees;
        this.cd_fees = cd_fees;
        this.r_fees = r_fees;
        this.ex_fees = ex_fees;
        this.g_fees = g_fees;
    }
    
    

    public double getS_fees() {
        return s_fees;
    }

    public void setS_fees(double s_fees) {
        this.s_fees = s_fees;
    }

    public double getCd_fees() {
        return cd_fees;
    }

    public void setCd_fees(double cd_fees) {
        this.cd_fees = cd_fees;
    }

    public double getR_fees() {
        return r_fees;
    }

    public void setR_fees(double r_fees) {
        this.r_fees = r_fees;
    }

    public double getEx_fees() {
        return ex_fees;
    }

    public void setEx_fees(double ex_fees) {
        this.ex_fees = ex_fees;
    }

    public double getG_fees() {
        return g_fees;
    }

    public void setG_fees(double g_fees) {
        this.g_fees = g_fees;
    }
    
    
    
    
   
            
    
}
